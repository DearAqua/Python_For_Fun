var = 'Viceroy'
 
print (var)          # 输出字符串
print (var[0:-1])    # 输出第一个到倒数第二个的所有字符
print (var[0])       # 输出字符串第一个字符
print (var[2:5])     # 输出从第三个开始到第五个的字符
print (var[2:])      # 输出从第三个开始的后的所有字符
print (var * 2)      # 输出字符串两次
print (var + "连接") # 连接字符串

print('Vice\roy')

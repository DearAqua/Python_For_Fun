str1 = "Viceroy Vittorio Veneto"
# 把所有字符中的小写字母转换成大写字母
print(str1.upper())          
# 把所有字符中的大写字母转换成小写字母
print(str1.lower())          
# 把第一个字母转化为大写字母，其余小写
print(str1.capitalize())     
# 把每个单词的第一个字母转化为大写，其余小写
print(str1.title())          

